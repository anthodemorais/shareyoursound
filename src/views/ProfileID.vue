<template>
  <div>
    <h2>{{ firstname }} {{ lastname }}</h2>
    <!-- <a v-bind:href="url + id">Utilisateur nÂ° {{ id }}</a></p> -->
    <strong>{{ email }}</strong>
    <img src="{{ picture }}" alt="{{ firstname }} {{ lastname }}" />
  </div>
</template>

<script>
import { ref } from 'vue';
import api from '../api';

function useMyProfileBlock() {
  const id = ref('');
  const firstname = ref('');
  const lastname = ref('');
  const email = ref('');
  const picture = ref('');

  api.getRequest('/user', (data) => {
    if (localStorage.getItem('token') === undefined) {
      this.getAccessToken();
    }

    console.log(localStorage.getItem('token'));
    if (data !== 'error') {
      id.value = data.user.id;
      firstname.value = data.user.firstname;
      lastname.value = data.user.lastname;
      email.value = data.user.email;
      picture.value = data.user.picture;
    }
  });

  return {
    id, firstname, lastname, email, picture,
  };
}
export default {
  setup() {
    return { ...useMyProfileBlock() };
  },
};
</script>
